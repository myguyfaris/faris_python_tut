'''
AND - if both operands are TRUE, then condition becomes TRUE
'''
a = 10
b = 5

if a < 20 and b < 10:
    print("The logical operand is TRUE")
else:
    print("The result is FALSE")

'''
OR - if any of the operands are TRUE, then the condition becomes TRUE
NOT - reverses the actual logical state of the given variable
'''
c = True
print(not c)